class LargestNumber
{
	public static void main(String[] args)
	{
		int firstNumber = 10;
		int secondNumber = 25;
		
		if (firstNumber > secondNumber)
		{
			System.out.println(firstNumber + " is the largest number");
		}
		else
		{
			System.out.println(secondNumber + " is the largest number");
		}
	}
}