class AssignmentOperator
{
	public static void main(String[] args)
	{
		int a = 10;
		
		a+=10;
		System.out.println("Additon using += : " + a);
		
		a-=5;
		System.out.println("Subraction using -= : " + a);
		
		a*=5;
		System.out.println("Multiplication using *= : " + a);
		
		a/=10;
		System.out.println("Division using /= : " + a);
		
		a%=5;
		System.out.println("Modules using %= : " + a);
		
	}
}